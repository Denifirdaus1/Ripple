  formatting,
  
  /// List tools (bullet, numbered, checkbox)
  list,
  
  /// Media tools (image, camera, voice)
  media,
  
  /// AI-powered tools
  ai,
  
  /// Utility tools (undo, redo, link, keyboard)
  utility,
}
